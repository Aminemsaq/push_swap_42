#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len_s1;
	char	*result;
	int		i;

	len_s1 = ft_strlen(s1);
	result = (char *)malloc((len_s1 + 1) * sizeof(char));
	if (!result)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
